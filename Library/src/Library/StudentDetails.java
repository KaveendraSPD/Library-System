    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            java.sql.Statement st = con.createStatement();
            java.sql.ResultSet rt = null;

            String id = jTextField1.getText();
            String name = jTextField2.getText();
            int year = Integer.parseInt(jComboBox1.getSelectedItem().toString());
            int semester = Integer.parseInt(jComboBox2.getSelectedItem().toString());
            String phone = jTextField3.getText();

            try {
                rt = st.executeQuery("SELECT Member_Id FROM `member` WHERE Member_Id='" + id + "'"); 
            } catch (java.sql.SQLException sqle) {
            } finally {
                if (id.equals("")) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Student ID cannot be empty.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                } else if (name.equals("")) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Student name cannot be empty.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                } else if (phone.equals("")) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Student contact number cannot be empty.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                } else if (!(phone.length() == 10 && phone.matches("[0-9]+"))) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Student contact number invalid.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                } else if (rt.next()) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Student details already exist.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                } else {
                    st.executeUpdate("INSERT INTO `member`(Member_Id,Name,Year,Semester,Phone) VALUES('" + id + "','" + name + "','" + year + "','" + semester + "','" + Integer.parseInt(phone) + "')");
                    javax.swing.JOptionPane.showMessageDialog(this, "Student details succesfully added.", "Successfull", javax.swing.JOptionPane.PLAIN_MESSAGE);
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jComboBox2.setSelectedIndex(0);
                    jTextField3.setText("");
                }
            }
        } catch (java.sql.SQLException sqle) {
            System.out.print("Connection failed");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

