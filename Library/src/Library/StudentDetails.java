    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
        try {
            String id = jTextField4.getText();
            java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            java.sql.Statement st = con.createStatement();
            java.sql.ResultSet rs = st.executeQuery("SELECT Member_Id,Name,Year,Semester,Phone FROM `member` WHERE Member_Id = '" + id + "'");
            jLabel12.setText("-");
            jLabel13.setText("-");
            jLabel14.setText("-");
            jLabel15.setText("-");
            jLabel16.setText("-");

            if (rs.next()) {
                if (rs.getString("Member_Id").equals(id)) {
                    jLabel12.setText(rs.getString("Member_Id"));
                    jLabel13.setText(rs.getString("Name"));
                    jLabel14.setText(rs.getString("Year"));
                    jLabel15.setText(rs.getString("Semester"));
                    jLabel16.setText("0" + rs.getString("Phone"));
                }
            }
        } catch (java.sql.SQLException sqle) {
            System.out.println("Connection failed");
        }
    }//GEN-LAST:event_jTextField4KeyReleased

