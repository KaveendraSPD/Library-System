    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String id = jTextField6.getText();
            String access = jTextField10.getText();
            java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            java.sql.Statement st = con.createStatement();
            java.sql.ResultSet rs1 = st.executeQuery("SELECT Accession_No FROM accessionno WHERE Accession_No NOT IN (SELECT Accession_No FROM loan)");
            if (rs1.next()) {
                while (rs1.getString(1).equals(access)) {
                    if (rs1.getString(1).equals(access)) {
                        java.sql.ResultSet rs2 = st.executeQuery("SELECT MAX(Borrow_Id) FROM loan");
                        rs2.next();
                        int bid = Integer.parseInt(rs2.getString(1));
                        bid++;
                        st.executeUpdate("INSERT INTO loan(Borrow_Id,Member_Id,Accession_No,Borrowed_Date,Due_Date,Loan_Status) VALUES ('" + bid + "','" + id + "','" + access + "',CURDATE(),DATE_ADD(CURDATE(),INTERVAL +10 DAY),'Not Returned')");
                        javax.swing.JOptionPane.showMessageDialog(this, "Borrow entry added successfully.", "Successfull", javax.swing.JOptionPane.PLAIN_MESSAGE);
                        jTextField7.setText("");
                        jTextField8.setText("");
                        jTextField9.setText("");
                        jTextField6.setText("");
                        jTextField10.setText("");
                    }
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "There are no copies available.", "Error", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (java.sql.SQLException sqle) {
            System.out.println("Connection failed");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            String id = jTextField11.getText();
            String access = jTextField5.getText();
            java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            java.sql.Statement st = con.createStatement();
            java.sql.ResultSet rs = st.executeQuery("SELECT Accession_No FROM loan WHERE Member_Id = '" + id + "'");
            if (rs.next()) {
                while (rs.getString(1).equals(access)) {
                    if (rs.getString(1).equals(access)) {
                        st.executeUpdate("UPDATE loan SET Return_Date=CURDATE(),Loan_Status='Returned' WHERE Accession_No='" + access + "'");
                        javax.swing.JOptionPane.showMessageDialog(this, "Book return successfully.", "Successfull", javax.swing.JOptionPane.PLAIN_MESSAGE);
                        jTextField5.setText("");
                        jTextField11.setText("");
                        jTable2.removeAll();
                    }
                }
            } else if ((id.equals("") || access.equals(""))) {
                javax.swing.JOptionPane.showMessageDialog(this, "Enter fields.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "There are no books to return.", "Error", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (java.sql.SQLException sqle) {
            System.out.println("Connection failed");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

