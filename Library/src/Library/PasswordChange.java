    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            java.sql.Statement st = con.createStatement();
            java.sql.ResultSet rs = st.executeQuery("SELECT Email FROM `user` WHERE Username='" + Login.getUsername() + "'");
            rs.next();

            String user = jTextField4.getText();
            String email = jTextField5.getText();
            char[] password = jPasswordField4.getPassword();

            if (user.equals("") || email.equals("") || java.util.Arrays.equals(password, "".toCharArray())) {
                javax.swing.JOptionPane.showMessageDialog(this, "Enter fields.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            } else if (email.equals(rs.getString(1)) && java.util.Arrays.equals(password, Login.getPassword())) {
                int confirm = javax.swing.JOptionPane.showConfirmDialog(null, "Do you want to change the username?");
                if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                    st.executeUpdate("UPDATE `user` SET Username='" + user + "' WHERE Username='" + Login.getUsername() + "'");
                    javax.swing.JOptionPane.showMessageDialog(this, "Username succesfully changed.", "Successfull", javax.swing.JOptionPane.PLAIN_MESSAGE);
                }
                jTextField4.setText("");
                jTextField5.setText("");
                jPasswordField4.setText("");
            } else if (!email.equals(rs.getString(1))) {
                javax.swing.JOptionPane.showMessageDialog(this, "Incorrect e-mail.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            } else if (!java.util.Arrays.equals(password, Login.getPassword())) {
                javax.swing.JOptionPane.showMessageDialog(this, "Incorrect password.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } catch (java.sql.SQLException sqle) {
            System.out.print("Connection failed");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

