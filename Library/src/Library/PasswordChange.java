    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            java.sql.Statement st = con.createStatement();

            char[] password = jPasswordField3.getPassword();
            char[] newPW = jPasswordField2.getPassword();
            char[] confirmNewPW = jPasswordField1.getPassword();

            if (java.util.Arrays.equals(password, "".toCharArray()) || java.util.Arrays.equals(newPW, "".toCharArray()) || java.util.Arrays.equals(confirmNewPW, "".toCharArray())) {
                javax.swing.JOptionPane.showMessageDialog(this, "Enter fields.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            } else if (java.util.Arrays.equals(password, Login.getPassword()) && java.util.Arrays.equals(newPW, confirmNewPW)) {
                int confirm = javax.swing.JOptionPane.showConfirmDialog(null, "Do you really want to change the password?");
                if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                    st.executeUpdate("UPDATE `user` SET Password='" + String.valueOf(newPW) + "' WHERE Username='" + Login.getUsername() + "'");
                    javax.swing.JOptionPane.showMessageDialog(this, "Password succesfully changed.", "Successfull", javax.swing.JOptionPane.PLAIN_MESSAGE);
                }
                jPasswordField3.setText("");
                jPasswordField2.setText("");
                jPasswordField1.setText("");
            } else if (!java.util.Arrays.equals(newPW, confirmNewPW)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Password mismatch.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Incorrect password.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } catch (java.sql.SQLException sqle) {
            System.out.print("Connection failed");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

