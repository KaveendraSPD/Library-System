    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        try {
            java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            java.sql.Statement st = con.createStatement();

            String isbn = jTextField2.getText();
            int isbn1 = Integer.parseInt(isbn);

            java.sql.ResultSet rs = st.executeQuery("SELECT B.*,A1.Accession_No,A2.Author FROM book B,accessionno A1,author A2,author_of_book A3 WHERE B.ISBN = '" + isbn1 + "' AND B.Book_No = A1.Book_No AND B.Book_No = A3.Book_No AND A3.Author_Id = A2.Author_Id");
            jLabel20.setText("-");
            jLabel21.setText("-");
            jLabel22.setText("-");
            jLabel23.setText("-");
            jComboBox1.removeAllItems();

            if (rs.next()) {
                if (rs.getInt("ISBN") == isbn1) {
                    jLabel20.setText(rs.getString("ISBN"));
                    jLabel21.setText(rs.getString("Title"));
                    jLabel22.setText(rs.getString("Author"));
                }
                java.sql.ResultSet rs2 = st.executeQuery("SELECT B.ISBN, COUNT(B.ISBN) FROM book B,accessionno A WHERE B.ISBN = '" + isbn1 + "' AND B.Book_No = A.Book_No AND A.Accession_No NOT IN ( SELECT L.Accession_No FROM loan L) GROUP BY B.ISBN ");
                if (rs2.next()) {
                    jLabel23.setText(rs2.getString("COUNT(B.ISBN)"));
                }
                java.sql.ResultSet rs3 = st.executeQuery("SELECT a.Accession_No FROM accessionno a INNER JOIN book b ON a.Book_No = b.Book_No WHERE b.ISBN = '" + isbn1 + "' AND Accession_No NOT IN (SELECT Accession_No FROM loan)");
                while (rs3.next()) {
                    jComboBox1.addItem(rs3.getString(1));
                }
            }
            rs = null;
        } catch (java.sql.SQLException sqle) {
            System.out.println("Connection failed");
        } catch (NumberFormatException e) {
        }
    }//GEN-LAST:event_jTextField2KeyReleased

